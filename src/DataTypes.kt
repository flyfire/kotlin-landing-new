/**
 * Iterate: Rubaye Lesson 2.8
 * var String Int
 * ? Null + Concatenation
 */

fun main(args: Array<String>) {

    var named = "JetBrains"
    println(named)

    var name:String = "Kotlin"
    println(name)

    var year:Int = 2017
    println(year)

    var tool:String
    tool = "IntelliJ IDEA"
    println(tool)

    var canary:String?
    canary = "Android Studio"
    var version:Int?
    version = 3
    println(canary + " " + version)

}